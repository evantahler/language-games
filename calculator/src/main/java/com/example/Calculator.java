package com.example;


public class Calculator {
  /**
   * I add stuff
   * @param a
   * @param b
   * @return
   */
  public float add(float a, float b) {
    return a + b;
  }

   /**
   * I subtract stuff
   * @param a
   * @param b
   * @return
   */
  public float subtract(float a, float b) {
    return a - b;
  }
}
